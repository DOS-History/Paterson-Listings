00770	; Description of 32-byte directory entry (same as returned by SEARCH FIRST
00780	; and SEARCH NEXT, functions 17 and 18).
00790	;
00800	; Location	bytes	Description
00810	;
00820	;    0		11	File name and extension ( 0E5H if empty)
00830	;   11		13	Zero field (for expansion)
00840	;   24		 2	Date. Bits 0-4=day, bits 5-8=month, bits 9-15=year-1980
00850	;   26		 2	First allocation unit ( < 4080 )
00860	;   28		 4	File size, in bytes (LSB first, 30 bits max.)
00870	;
00880	; The File Allocation Table uses a 12-bit entry for each allocation unit on
00890	; the disk. These entries are packed, two for every three bytes. The contents
00900	; of entry number N is found by 1) multiplying N by 1.5; 2) adding the result
00910	; to the base address of the Allocation Table; 3) fetching the 16-bit word at
00920	; this address; 4) If N was odd (so that N*1.5 was not an integer), shift the
00930	; word right four bits; 5) mask to 12 bits (AND with 0FFF hex). Entry number
00940	; zero is used as an end-of-file trap in the OS and as a flag for directory
00950	; entry size (if SMALLDIR selected). Entry 1 is reserved for future use. The
00960	; first available allocation unit is assigned entry number two, and even
00970	; though it is the first, is called cluster 2. Entries greater than 0FF8H are
00980	; end of file marks; entries of zero are unallocated. Otherwise, the contents
00990	; of a FAT entry is the number of the next cluster in the file.
